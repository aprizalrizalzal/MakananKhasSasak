package me.aprizal.makanankhassasak.model;

public class ModelMenu {
    private int imageMenu;
    private String menuEat;
    private String detailMenu;
    private String detailLocation;

    public int getImageMenu() {
        return imageMenu;
    }

    public void setImageMenu(int imageMenu) {
        this.imageMenu = imageMenu;
    }

    public String getMenuEat() {
        return menuEat;
    }

    public void setMenuEat(String menuEat) {
        this.menuEat = menuEat;
    }

    public String getDetailMenu() {
        return detailMenu;
    }

    public void setDetailMenu(String detailMenu) {
        this.detailMenu = detailMenu;
    }

    public String getDetailLocation() {
        return detailLocation;
    }

    public void setDetailLocation(String detailLocation) {
        this.detailLocation = detailLocation;
    }
}
